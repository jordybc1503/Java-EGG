public class DeclararVariable {

    public static void main(String[] args){
        // Step 1: Declare a variable
        boolean soyBueno = true;
        char letra = 'A';
        String palabra = "Te amo Alexa";
        int edad = 29;
        long distanciaaCasa = 456545454L;
        float tamanoDePerro = 45.56f;

        // Step 2: 
        System.out.println(soyBueno);
        System.out.println(letra);
        System.out.println(palabra);
        System.out.println(edad);
        System.out.println(distanciaaCasa);
        System.out.println(tamanoDePerro);



    }
    
}
