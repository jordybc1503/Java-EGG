public class BasicsOperations {
    public static void main(String[] args){
        float num1 = 5;
        float num2 = 2;

        System.out.println("Sum of num1 + num2: " + (num1+ num2));
        System.out.println("Rest of num1 - num2: " + (num2 - num1));
        System.out.println("Module of num1%num2: " + (num1%num2));
        System.out.println("Division of num1/num2: " + (num1/num2));
    }
}
